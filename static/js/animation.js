document.addEventListener('DOMContentLoaded', function() {
    // Konfigurasi particles.js
    particlesJS('particles-js', {
        particles: {
            number: {
                value: 100,
                density: {
                    enable: true,
                    value_area: 800
                }
            },
            color: {
                value: '#0E2148'
            },
            shape: {
                type: 'circle'
            },
            opacity: {
                value: 0.5,
                random: true
            },
            size: {
                value: 3,
                random: true
            },
            line_linked: {
                enable: true,
                distance: 150,
                color: '#0E2148',
                opacity: 0.4,
                width: 1
            },
            move: {
                enable: true,
                speed: 2,
                direction: 'none',
                random: true,
                straight: false,
                out_mode: 'out',
                bounce: false
            }
        },
        interactivity: {
            detect_on: 'canvas',
            events: {
                onhover: {
                    enable: true,
                    mode: 'grab'
                },
                onclick: {
                    enable: true,
                    mode: 'push'
                },
                resize: true
            }
        },
        retina_detect: true
    });

    // Animasi melayang untuk gambar mobil
    const carImage = document.querySelector('.car-image');
    if (carImage) {
        setInterval(() => {
            carImage.style.transform = 'translateY(-10px)';
            setTimeout(() => {
                carImage.style.transform = 'translateY(0)';
            }, 1000);
        }, 2000);
    }
});